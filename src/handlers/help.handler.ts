// src/handlers/help.handler.ts
// Обработчик команды /help

import { EMOJI } from '../config/constants.js';
import type { BotContext } from '../types/bot.js';

export async function handleHelp(ctx: BotContext): Promise<void> {
    const helpMessage = `
${EMOJI.BOOK} **Помощь по использованию бота**

**Доступные команды:**

/start - Начать регистрацию
/schedule - Показать расписание на неделю
/help - Показать это сообщение

**Как пользоваться:**

1. Используйте /start для регистрации
2. Согласитесь с правилами сервиса
3. Выберите подразделение, курс и группу
4. Готово! Теперь вы можете смотреть расписание

**Возникли проблемы?**

Если бот не отвечает, попробуйте:
- Перезапустить его командой /start
- Проверить подключение к интернету
- Написать в поддержку

**Примечание:**
Расписание обновляется автоматически. Бот показывает актуальную информацию на текущую неделю (четную или нечетную).
  `.trim();

    await ctx.reply(helpMessage, { parse_mode: 'Markdown' });
}